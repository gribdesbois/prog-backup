#include <iostream>

using namespace std;

int main()
{
    for (int i=1 ; i <= 10 ; i++)
    {
        cout    <<  "\tTABLE DE   " << i <<"\n";

        for (int j=2 ; j <= 10 ; j++)
        {
            cout    <<  "\n" << j << " * " << i << " = " << j*i <<endl;
        }
    }

cout<<"DASD";
    return 0;
}
